package day1;

public class Operators {
    public static void main(String[] args) {

        int x = 20;
        int y = 30;

        int sum = x+y;
        System.out.println("Sum = " + sum);
        int sub = x-y;
        System.out.println("Sub = " + sub);
        int div = x/y;
        System.out.println("Div = " + div);
        int mul = x*y;
        System.out.println("Mul  = " + mul);
        int rem = x%y;
        System.out.println("Rem = " + rem);


    }
}
